<?php

namespace EventStore\StreamFeed;

/**
 * Class Event
 * @package EventStore\StreamFeed
 */
class Event
{
    /**
     * @var array
     */
    private $data;

    /**
     * @param array $data
     */
    public function __construct(array $data)
    {
        $this->data = $data;
    }

    /**
     * @return array
     */
    public function getData()
    {
        return $this->data;
    }
}
